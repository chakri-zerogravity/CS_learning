import java.util.Scanner;
public class switch_s1 {
    
    public static void main(String[] args) {

        try (Scanner s1 = new Scanner(System.in)) {
            int a = s1.nextInt();

            switch (a){
                case 1:
                System.out.println("This is case 2");

                break;
      

            case 2:
            System.out.println("This is case 2");

            break;

            }
        }
        
    }
}
